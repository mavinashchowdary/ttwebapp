// push and pop

// push helps to add elements at last

// pop removes element from last

// shift and unshift

// shift helps to remove elements from start unlike pop

// unshift helps to add elements at first unlike push

const myArray = [1,2,3,4];

const x = myArray.shift();

console.log(x);

console.log(myArray);

myArray.unshift(1);

console.log(myArray);

